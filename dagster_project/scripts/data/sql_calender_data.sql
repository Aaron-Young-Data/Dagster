SELECT
    EVT.EVENT_NAME,
    EVT.FCST_LOCATION,
    LOC.LATITUDE,
    LOC.LONGITUDE
FROM
    DIM_EVENT EVT
LEFT JOIN DIM_LOCATION LOC
    ON EVT.FCST_LOCATION = LOC.FCST_LOCATION
WHERE
    EVENT_YEAR = '{partitioned_date_year}'
GROUP BY
    EVT.EVENT_NAME,
    EVT.FCST_LOCATION,
    LOC.LATITUDE,
    LOC.LONGITUDE

SELECT
    EVT.EVENT_NAME,
    EVT.FCST_LOCATION,
    TRK.LATITUDE,
    TRK.LONGITUDE
FROM
    REFERENCE.DIM_EVENT EVT
LEFT JOIN REFERENCE.DIM_TRACK TRK
    ON EVT.TRACK_CD = TRK.TRACK_CD
WHERE
    EVENT_YEAR = '{partitioned_date_year}'
GROUP BY
    EVT.EVENT_NAME,
    EVT.FCST_LOCATION,
    TRK.LATITUDE,
    TRK.LONGITUDE
